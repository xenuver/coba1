    private void showTable(){
        DefaultTableModel tb = new DefaultTableModel();
        tb.addColumn("Barang");
        tb.addColumn("Harga");
        tb.addColumn("QTY");
        tb.addColumn("Jumlah");
        
        table.setModel(tb);
        
        try {
            res = stat.executeQuery("SELECT * FROM tb_soal1");
            while(res.next()){
                Object[] data = {
                    res.getString("nama_barang"),
                    res.getString("harga"),
                    res.getInt("quantity"),
                    res.getInt("jumlah"),
                };
                tb.addRow(data);
            }
        } 
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

