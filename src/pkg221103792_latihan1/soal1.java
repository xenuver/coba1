    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String hargaStr = txtHarga.getText();
        String qtyStr = txtQty.getText();

        double hargas = Double.parseDouble(hargaStr);
        int qty = Integer.parseInt(qtyStr);
        jumlah = (int) (hargas * qty);
        
        try {
            stat.executeUpdate("INSERT INTO tb_soal1 VALUES ("
                + "'" + CmbBarang.getSelectedItem().toString() + "',"
                + "'" + txtHarga.getText() + "',"
                + "'" + txtQty.getText() + "',"
                + "'" + jumlah + "')"
            );
            showTable();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
        } 
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

