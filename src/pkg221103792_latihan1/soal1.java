    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    String query = "SELECT SUM(jumlah) AS total_harga FROM tb_soal1";
        try (
             ResultSet resultSet = stat.executeQuery(query)) {
            if (resultSet.next()) {
                double totalPrice = resultSet.getDouble("total_harga");
                txtSubtotal.setText(String.valueOf(totalPrice));
            } else {
                txtSubtotal.setText("0");
            }
            String ppns = txtSubtotal.getText();
            double ppn = Double.parseDouble(ppns) * 0.1;
            
            if (jCheckBox1.isSelected()) {
                txtPPN.setText(""+ppn);
            } else {
                txtPPN.setText("0");
            }
            
            String subtotal = txtPPN.getText();
            double subtotals = Double.parseDouble(subtotal);
            double totals = Double.parseDouble(ppns) - subtotals;
            txtTotal.setText(""+totals);
            
            String bayar = txtBayar.getText();
            double bayars = Double.parseDouble(bayar) - totals;
            txtKembalian.setText(""+bayars);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

